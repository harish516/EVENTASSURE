/** Hold the fields to represent an address object */  
export class AddressModel {

    /** Address line 1 usually holds number and house name/road*/    
    address1: string;

    /** Address line 2 */
    address2: string;

    /** Address line 3 holds the town the person lives in */
    address3: string;

    /** Address line 4 usually had the county of the person */
    address4: string;

    /** Postcode identifier to the address */
    postCode: string;
    
}
